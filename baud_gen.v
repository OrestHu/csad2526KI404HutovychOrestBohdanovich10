module baud_gen #
(
    parameter CLK_FREQ = 50000000,  // ??????? ?????????? ?????, ??
    parameter BAUD_RATE = 9600      // ?????? ????????? UART, ???
)
(
    input  wire clk,     // ????????? ???????? ??????
    input  wire reset,   // ?????????? ????????
    output reg  tick     // ???????? ??????? baud_tick
);

    // ????????? ?????? ??? ????? baud_tick
    localparam integer DIVISOR = CLK_FREQ / BAUD_RATE;
    reg [31:0] counter;

    always @(posedge clk or posedge reset) begin
        if (reset) begin
            counter <= 0;
            tick <= 0;
        end else begin
            if (counter >= DIVISOR - 1) begin
                counter <= 0;
                tick <= 1'b1;    // ???????? ???????
            end else begin
                counter <= counter + 1;
                tick <= 1'b0;
            end
        end
    end
endmodule
